

export function banhoGato(racaoGato, tipoBanho){
    return `Gato ${racaoGato} fez um banho tipo: ${tipoBanho}`
}


export function tosaGato(racaoGato, tipoTosa){
    return `Gato ${racaoGato} fez uma tosa tipo: ${tipoTosa}`
}

export function alimentarGato(racaoGato, tipoRacao){
    return `Gato ${racaoGato} comeu uma ração tipo: ${tipoRacao}`
}